package kr.or.profit.vo;

public class PopularVO {
	private String popularSearchSeq;
	private String searchWord;
	private String searchCnt;
	private String inUserId;
	private String inDate;
	private String upUserId;
	private String upDate;
	
	public String getPopularSearchSeq() {
		return popularSearchSeq;
	}
	public void setPopularSearchSeq(String popularSearchSeq) {
		this.popularSearchSeq = popularSearchSeq;
	}
	public String getSearchWord() {
		return searchWord;
	}
	public void setSearchWord(String searchWord) {
		this.searchWord = searchWord;
	}
	public String getSearchCnt() {
		return searchCnt;
	}
	public void setSearchCnt(String searchCnt) {
		this.searchCnt = searchCnt;
	}
	public String getInUserId() {
		return inUserId;
	}
	public void setInUserId(String inUserId) {
		this.inUserId = inUserId;
	}
	public String getInDate() {
		return inDate;
	}
	public void setInDate(String inDate) {
		this.inDate = inDate;
	}
	public String getUpUserId() {
		return upUserId;
	}
	public void setUpUserId(String upUserId) {
		this.upUserId = upUserId;
	}
	public String getUpDate() {
		return upDate;
	}
	public void setUpDate(String upDate) {
		this.upDate = upDate;
	}
	
	
}
